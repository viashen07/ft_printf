#include "libft.h"

/*
** Frees the thing created by ft_strsplit
*/

void			ft_strsplit_free(char **thing)
{
	size_t		i;

	i = 0;
	while (thing[i])
	{
		free(thing[i]);
		i++;
	}
	free(thing);
}
